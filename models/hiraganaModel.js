const mongoose = require("mongoose");

const hiraganaSchema = new mongoose.Schema({
  kana: {
    type: String,
    required: true,
  },
  roumaji: {
    type: String,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
});

const Hiragana = mongoose.model("hiragana", hiraganaSchema, "hiragana");

module.exports = Hiragana;

